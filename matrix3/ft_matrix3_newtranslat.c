t_matrix3	*ft_matrix3_newtranslat(t_vector3 *v)
{
	t_matrix3 *m;

	if (!(m = ft_matrix3_new()))
		return (NULL);
	m->m[0][0] = v->x;
	m->m[1][1] = v->y;
	m->m[2][2] = v->z;
	return (m);
}
