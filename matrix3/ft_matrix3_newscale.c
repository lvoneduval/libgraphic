t_matrix3	*ft_matrix3_newscale(float f)
{
	t_matrix3 *m;

	if (!(m = ft_matrix3_new()))
		return (NULL);
	m->m[0][0] = f;
	m->m[1][1] = f;
	m->m[2][2] = f;
	return (m);
}
