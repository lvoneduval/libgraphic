#include "libgraphic.h"

t_vertex3	*ft_matrix3_multv(t_matrix3 *m, t_vertex3 *v)
{
	t_vertex3	*v1;

	if (!(v1 = ft_vertex3_new()))
		return (NULL);
	v1->x = (m->m[0][0] * v->x) + (m->m[0][1] * v->y) +
		(m->m[0][2] * v->z) + (m->m[0][3] * v->w);
	v1->y = (m->m[1][0] * v->x) + (m->m[1][1] * v->y) +
		(m->m[1][2] * v->z) + (m->m[1][3] * v->w);
	v1->z = (m->m[2][0] * v->x) + (m->m[2][1] * v->y) +
		(m->m[2][2] * v->z) + (m->m[2][3] * v->w);
	v1->w = (m->m[3][0] * v->x) + (m->m[3][1] * v->y) +
		(m->m[3][2] * v->z) + (m->m[3][3] * v->w);
	return (v1);
}
