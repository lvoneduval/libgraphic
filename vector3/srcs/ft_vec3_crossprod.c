#include "vector3.h"

t_vector3	ft_vec3_crossprod(t_vector3 v1, t_vector3 v2)
{
	t_vector3	v;

	v.x = (v1.y * v2.z) - (v1.z * v2.y);
	v.y = (v1.z * v2.x) - (v1.x * v2.z);
	v.z = (v1.x * v2.y) - (v1.y * v2.x);
	return (v);
}
