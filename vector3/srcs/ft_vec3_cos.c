#include "vector3.h"

t_vector3	ft_vec3_cos(t_vector3 v1, t_vector3 v2)
{
	return (ft_vec3_dotprod(v1, v2) /
			(ft_vec3_magnitude(v1) * ft_vec3_magnitude(v2)));
}
