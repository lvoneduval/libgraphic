#include "vector3.h"

t_vector3	ft_vec3_sub(t_vector3 v1, t_vector3 v2)
{
	t_vector3	v;

	v.x = v1.x - v2.x;
	v.y = v1.y - v2.y;
	v.z = v1.z - v2.z;
	return (v);
}
