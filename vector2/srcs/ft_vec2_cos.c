#include "vector2.h"

t_vector2	ft_vec2_cos(t_vector2 v1, t_vector2 v2)
{
	return (ft_vec2_dotprod(v1, v2) /
			(ft_vec2_magnitude(v1) * ft_vec2_magnitude(v2)));
}
