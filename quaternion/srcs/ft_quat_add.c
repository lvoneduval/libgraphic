#include "4ternion.h"

t_4ternion	ft_quat_add(t_4ternion v1, t_4ternion v2)
{
	t_4ternion	v;

	v.x = v1.x + v2.x;
	v.y = v1.y + v2.y;
	v.z = v1.z + v2.z;
	v.w = v1.w + v2.w;
	return (v);
}
