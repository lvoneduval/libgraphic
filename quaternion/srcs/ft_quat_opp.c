#include "4ternion.h"

t_4ternion	ft_quat_opp(t_4ternion v)
{
	t_4ternion	v1;

	v1.x = -v.x;
	v1.y = -v.y;
	v1.z = -v.z;
	v1.z = v.w;
	return (v1);
}
