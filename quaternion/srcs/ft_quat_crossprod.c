#include "4ternion.h"

t_4ternion	ft_quat_crossprod(t_4ternion v1, t_4ternion v2)
{
	t_4ternion	v;

	v.x = (v1.y * v2.z) - (v1.z * v2.y);
	v.y = (v1.z * v2.x) - (v1.x * v2.z);
	v.z = (v1.x * v2.y) - (v1.y * v2.x);
	v.w = 0.0;
	return (v);
}
